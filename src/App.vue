<template>
  <div id="app">
    <img alt="Vue logo" :src="getLogo">
    <HelloWorld msg="Welcome to Your Vue.js App"/>
  </div>
</template>

<script>
import HelloWorld from "./components/HelloWorld.vue";

export default {
  name: "app",
  components: {
    HelloWorld
  },
  data() {
    return { logo: "logo.png" };
  },
  computed: {
    getLogo() {
      return require(`@/assets/${this.logo}`);
    }
  }
};
</script>

